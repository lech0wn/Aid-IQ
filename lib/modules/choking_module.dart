# MODULE OVERVIEW

This module provides essential knowledge and skills for recognizing and managing choking emergencies. Choking occurs when an object blocks the airway, preventing normal breathing. Quick and appropriate action can save a life. This module covers both partial and complete airway obstruction and teaches the proper techniques for back blows and abdominal thrusts (Heimlich maneuver).

