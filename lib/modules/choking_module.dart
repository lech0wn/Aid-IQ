## LESSON 1: UNDERSTANDING CHOKING

### What is Choking?

Choking occurs when a foreign object becomes lodged in the throat or windpipe, blocking the flow of air to the lungs.

**Common Causes:**

- Food (especially meat, hot dogs, hard candy, nuts)
- Small objects (toys, coins, buttons)
- Eating too quickly
- Not chewing food properly
- Talking or laughing while eating

### Universal Sign of Choking

A person who is choking will often:

- Clutch their throat with one or both hands (universal choking sign)
- Have a panicked or distressed expression
- Be unable to speak or make sounds
- Turn blue or pale

**Remember**: If someone is clutching their throat, ask "Are you choking?" If they nod but cannot speak, immediate action is needed.

## LESSON 2: TYPES OF AIRWAY OBSTRUCTION

### Partial Obstruction (Mild Choking)

**Signs and Symptoms:**

- The patient can usually speak
- The patient can cry (in children)
- The patient can cough
- The patient can breathe (though may be wheezing or struggling)
- Air is still getting through, though not fully

**Key Point**: The patient may be able to clear the blockage themselves through coughing.

### Complete Obstruction (Severe Choking)

**Signs and Symptoms:**

- The patient cannot speak
- The patient cannot cough effectively
- The patient cannot breathe
- May make high-pitched sounds or no sound at all
- Skin may turn blue (cyanosis), especially around lips and face
- May clutch throat with hands
- Will eventually become unconscious without help

**Key Point**: This is a life-threatening emergency requiring immediate intervention.

## LESSON 3: MANAGING PARTIAL OBSTRUCTION

### Step 1: Encourage Coughing

**What to do:**

- Stay calm and reassure the person
- Encourage them to keep coughing to clear the blockage
- Coughing is the body's natural and most effective way to clear an obstruction

**Why it works:**

- Coughing creates strong airflow that can dislodge the object
- It's safer than manual intervention when the person can still breathe

### Step 2: Ask Them to Spit Out the Object

**What to do:**

- If the object is visible in their mouth, ask them to spit it out
- Stay with them and monitor their condition

**Important**: Do NOT put your fingers in their mouth to help them, as they may bite you accidentally during distress or panic.

### Step 3: Monitor Closely

**What to watch for:**

- If coughing becomes weak or stops
- If breathing becomes more difficult
- If the person cannot speak anymore
- If the person starts to turn blue

**Action**: If the partial obstruction becomes a complete obstruction, immediately begin back blows.

### Step 4: Start Back Blows if Coughing Does Not Work

If the person continues to choke and coughing is not effective, proceed to back blows (see Lesson 4).

## LESSON 4: MANAGING COMPLETE OBSTRUCTION - BACK BLOWS

### When to Use Back Blows

Use back blows when:

- The person cannot speak, cough, or breathe
- Coughing is not effective in clearing the blockage
- The obstruction is complete

### How to Perform Back Blows

**Step 1: Position Yourself**

- Stand behind the person and slightly to one side
- For a child, you may need to kneel

**Step 2: Support Their Chest**

- Place one hand on their chest for support
- This prevents them from falling forward

**Step 3: Lean Them Forward**

- Lean the person forward so their upper body is bent at the waist

**Why**: This position allows gravity to help—the object blocking the airway will come out of their mouth rather than moving further down

**Step 4: Deliver Back Blows**

- Use the heel of your other hand
- Give up to 5 sharp blows between the shoulder blades
- Each blow should be firm and delivered with force
- Aim for the area between the shoulder blades

**Step 5: Check the Mouth**

- After each blow, quickly check the casualty's mouth
- If you see the object, ask them to spit it out
- Remove anything that may be lodged there (only if clearly visible and easy to remove)
- Do NOT perform a blind finger sweep

### Important Safety Notes

- Each back blow should be separate and distinct, not continuous patting
- Use enough force to dislodge the object
- Check between blows to see if the object has been expelled

## LESSON 5: MANAGING COMPLETE OBSTRUCTION - ABDOMINAL THRUSTS

### When to Use Abdominal Thrusts

Use abdominal thrusts (also known as the Heimlich maneuver) if:

- The casualty is still choking after 5 back blows
- Back blows have not dislodged the object

### How to Perform Abdominal Thrusts (Heimlich Maneuver)

**Step 1: Position Yourself**

- Stand behind the person
- Wrap your arms around their waist
- Position yourself so you can deliver thrusts effectively

**Step 2: Make a Fist**

- Make a fist with one hand
- Place it slightly above the person's navel (belly button)
- Position it below the ribcage

**Step 3: Grasp Your Fist**

- Place your other hand over your fist
- Hold firmly

**Step 4: Deliver Abdominal Thrusts**

- Pull inward and upward with a quick, forceful motion
- The movement should be like a "J" shape—inward and upward
- Give up to 5 abdominal thrusts
- Each thrust should be separate and distinct

**Step 5: Check the Mouth**

- After the thrusts, quickly check the casualty's mouth
- Remove any dislodged pieces if visible and easy to remove
- Check if the person can breathe

### Important Safety Considerations

**Do NOT perform abdominal thrusts on:**

- Infants under 1 year old (use back blows and chest thrusts instead)
- Pregnant women (use chest thrusts instead)
- Very obese individuals where you cannot wrap your arms around (use chest thrusts instead)

**Caution:**

- Abdominal thrusts can cause internal injuries
- Anyone who receives abdominal thrusts should be examined by a doctor afterward, even if the object is dislodged

## LESSON 6: ALTERNATING BETWEEN BACK BLOWS AND ABDOMINAL THRUSTS

### The Cycle

If the blockage is not cleared after the first round:

Repeat the following cycle:

- 5 back blows → Check mouth
- 5 abdominal thrusts → Check mouth
- 5 back blows → Check mouth
- 5 abdominal thrusts → Check mouth

Continue alternating between 5 back blows and 5 abdominal thrusts until:

- The blockage is dislodged
- The object comes out
- The person can breathe, speak, or cough effectively
- The person becomes unconscious (if this happens, call emergency services immediately and begin CPR if trained)

### Why Alternate?

- Different techniques may work for different types of obstructions
- Alternating increases the chances of dislodging the object
- Each technique uses different mechanisms (gravity + force vs. abdominal pressure)

## LESSON 7: SPECIAL CONSIDERATIONS AND WHEN TO CALL FOR HELP

### Call Emergency Services If:

- The person becomes unconscious
- The obstruction cannot be cleared after several cycles
- You are alone and need help
- The person is pregnant, obese, or an infant (special techniques required)
- The person has difficulty breathing even after the object is removed

**Important**: If possible, have someone else call for emergency help while you continue to assist the choking person.

### If the Person Becomes Unconscious

- Lower them to the ground carefully
- Call emergency services immediately (or have someone else call)
- Begin CPR if you are trained (chest compressions can help dislodge the object)
- Check the mouth before giving rescue breaths and remove any visible objects

### After the Obstruction is Cleared

Even if the choking episode is resolved:

- Encourage the person to seek medical attention
- Abdominal thrusts can cause internal injuries that may not be immediately apparent
- Watch for signs of complications (difficulty breathing, abdominal pain, vomiting blood)

### Prevention Tips

To prevent choking:

✓ Cut food into small pieces, especially for children and elderly

✓ Chew food thoroughly before swallowing

✓ Avoid talking or laughing with food in your mouth

✓ Supervise young children during meals

✓ Keep small objects away from young children

✓ Avoid giving high-risk foods to young children (whole grapes, nuts, hard candy, popcorn)

✓ Eat slowly and mindfully

