## LESSON 1: UNDERSTANDING STRAINS

A strain is an injury to a muscle or tendon (the tissue that connects muscle to bone). It usually happens when the muscle or tendon is:

- Overstretched
- Overused
- Forced to work too hard or too suddenly

**Common signs of a strain:**

- Pain in the affected area
- Swelling
- Muscle weakness or difficulty moving the part
- Possible muscle spasm

Strains commonly affect areas like the back, hamstrings, shoulders, and calves.

## LESSON 2: FIRST AID STEPS FOR STRAINS

Use the following steps to provide basic first aid care for a strain:

### 1. Rest the Strain

**What to do**: Stop using the injured body part.

**Why**: Rest helps prevent further damage and allows the tissue to begin healing.

Avoid sports, lifting, or any activities that cause pain in the strained area.

### 2. Ice

**What to do**: Apply ice or a cold pack to the strained area.

**How:**

- Wrap the ice in a cloth or towel (never apply directly to the skin).
- Apply for about 15–20 minutes at a time.

**Why**: Ice helps dull the pain and reduce swelling, especially in the first 48 hours.

### 3. Compress the Strain

**What to do**: Apply compression to the strained area using an elastic bandage.

**Why**: Compression helps reduce swelling and provides support.

**Important:**

- Make sure the bandage is snug but not too tight.
- If there is numbness, tingling, increased pain, or color change, loosen the bandage.

### 4. Elevate

**What to do**: Elevate the affected area.

**How:**

- Raise the injured limb above heart level using pillows or cushions.

**Why**: Elevation helps reduce pain and swelling by allowing fluids to drain away from the injured area.

### 5. Pain Relievers

**What to do**: Take over-the-counter pain relievers to minimize pain.

**Examples**: Paracetamol/acetaminophen or ibuprofen (follow package instructions).

**Why**: Pain relievers help make the person more comfortable while the strain heals.

## LESSON 3: WHEN TO SEEK PROFESSIONAL HELP

Not all strains heal completely with home care alone. Some need rehabilitation through physical therapy.

### Professional Help and Physical Therapy

**When it's needed:**

- Pain remains strong after several days.
- There is significant weakness or difficulty using the muscle.
- Swelling or stiffness does not improve.

**Why it helps:**

- Physical therapy helps the tissue heal properly.
- It helps the strained area regain its former strength and flexibility.
- It reduces the risk of future injury.

**Seek medical or professional help if:**

- You are unsure about the severity of the injury.
- You suspect a tear or more serious damage.
- The person cannot move or use the strained area normally.

