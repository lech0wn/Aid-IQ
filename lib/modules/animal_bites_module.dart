## LESSON 1: UNDERSTANDING ANIMAL BITES

### Types of Animal Bites

Animal bites can come from various sources:

**Domestic Animals:**

- Dogs
- Cats
- Rodents (hamsters, rats, etc.)

**Wild Animals:**

- Bats
- Raccoons
- Foxes
- Skunks
- Snakes

**Rabid Animals:**

- Any mammal that may carry the rabies virus

### Risks Associated with Animal Bites

**1. Infection**

- Animal mouths contain bacteria that can cause serious infections
