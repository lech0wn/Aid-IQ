## LESSON 2: FIRST AID FOR GENERAL ANIMAL BITES (DOGS, CATS, ETC.)

### Step 1: Wash the Bitten Area Thoroughly

**What to do:**

- Wash the wound immediately with soap and water
- Rinse thoroughly under running water for several minutes
- This is the single most important step to prevent infection

**Why it's important:**

- Removes bacteria and contaminants from the wound
- Reduces the risk of infection significantly

### Step 2: Apply Antibiotic Cream

**What to do:**

- After cleaning and drying the wound, apply a thin layer of antibiotic cream or ointment

**Why it's important:**

- Provides additional protection against infection
- Keeps the wound moist for better healing

### Step 3: Cover the Wound

**What to do:**

- Cover the bite with a clean bandage or sterile dressing

**Why it's important:**

- Protects the wound from dirt and bacteria
- Prevents further contamination

### Step 4: Observe for Signs of Infection

**What to watch for:**

- Redness spreading from the wound
- Increased warmth around the area
- Swelling
- Pus or drainage
- Increased pain
- Fever
- Red streaks extending from the wound

**Action**: If any signs of infection appear, seek medical attention immediately.

### Step 5: Do Not Scratch or Rub the Wound

**Why it's important:**

- Scratching can introduce new bacteria
- Can damage healing tissue
- May worsen infection
- Can increase scarring

**Tip**: If itching occurs during healing, this is normal, but resist the urge to scratch.

## LESSON 3: RABIES PREVENTION

### When Rabies is a Concern

Rabies risk is higher with:

- Bites from wild animals (bats, raccoons, foxes, skunks)
- Bites from stray or unknown dogs or cats
- Animals acting strangely or aggressively
- Any animal that cannot be observed for signs of rabies

### Rabies Post-Exposure Prophylaxis (PEP)

**What it is:**

- A series of shots given over the next few weeks after a bite that puts you at risk for rabies

**How it works:**

- Prevents the rabies virus from causing disease
- Must be started as soon as possible after exposure
- Highly effective when given promptly

**Important:**

- You MUST complete the full cycle of shots
- Do not skip appointments
- Follow the doctor's instructions exactly
- Rabies is almost always fatal once symptoms appear, but preventable with proper vaccination

**The doctor will tell you:**

- When to get each shot
- How many shots you need
- What to watch for

## LESSON 4: SPECIAL CONSIDERATIONS FOR SNAKE BITES

### Critical Difference: Do NOT Wash Snake Bites

**Why:**

- Traces of venom left on the skin can help medical personnel identify the type of snake
- Identification helps determine the appropriate antivenom treatment

### Immediate Actions for Snake Bites

**1. Seek Medical Help Immediately**

- Call emergency services or get to a hospital as fast as possible
- Snake bites are medical emergencies

**2. Use Pressure Immobilization Bandage**

- Apply a firm bandage over the bite site
- Wrap the entire limb (like wrapping a sprained ankle)
- The bandage should be firm but not cut off circulation
- This slows the spread of venom through the lymphatic system

**3. Splint the Limb**

- Keep the bitten limb still and immobilized
- Use a splint or any rigid object to prevent movement
- Movement can spread venom faster

**4. Keep the Person Calm and Still**

- Physical activity increases venom spread
- Have the person lie down if possible
- Keep the bitten area at or below heart level

### Special Case: Snake Bite on the Torso

**Important Consideration:**

- If the bite is on the torso (chest, abdomen, back), bandaging is still recommended
- Make sure the bandaging does not restrict breathing
- Monitor the person's breathing continuously
- Loosen bandages if breathing becomes difficult

### What NOT to Do for Snake Bites

❌ Do NOT wash the bite area

❌ Do NOT cut the wound

❌ Do NOT try to suck out the venom

❌ Do NOT apply ice

❌ Do NOT apply a tourniquet

❌ Do NOT give the person food, drink, or medication unless directed by medical personnel

## LESSON 5: TETANUS PREVENTION

### What is Tetanus?

Tetanus is a serious bacterial infection that affects the nervous system and causes painful muscle contractions, particularly in the jaw and neck.

### When a Tetanus Shot is Needed

You may need a tetanus shot if:

- You have not received one in the last 5 years
- The wound is deep or dirty
- The bite is from an animal

**Action:**

- Inform your doctor of your vaccination history
- Get a tetanus booster if recommended
- Keep a record of your tetanus vaccinations

## LESSON 6: PAIN MANAGEMENT AND ANTIBIOTICS

### Pain Management

**What to do:**

- Ask the doctor if you can take over-the-counter pain relievers to ease the pain
- Common options: acetaminophen (Tylenol) or ibuprofen (Advil, Motrin)

**Important:**

- Always consult with a doctor before taking pain medication
- Follow dosage instructions
- Some medications may interact with other treatments

### Antibiotic Treatment

**If prescribed antibiotics:**

**Critical Rules:**

- **Take them at the given time**
  - Set reminders if needed
  - Take with or without food as directed

- **Complete the full course**
  - Do NOT stop taking antibiotics even if you feel better
  - Stopping early can lead to antibiotic resistance
  - Infection may return stronger

- **Follow all instructions**
  - Take the exact dose prescribed
  - Don't skip doses
  - Don't share antibiotics with others

**Why it matters:**

- Ensures complete elimination of bacteria
- Prevents complications
- Reduces risk of antibiotic-resistant infections

## LESSON 7: WHEN TO SEEK MEDICAL ATTENTION

### Seek Immediate Medical Help If:

- The bite is from a wild animal or unknown animal
- The bite is deep or large
- Bleeding cannot be controlled
- The bite is on the face, neck, hands, or genitals
- The bite is from a snake
- You suspect rabies exposure
- Signs of infection develop
- You have not had a tetanus shot in 5+ years
- The animal was acting strangely or aggressively

### Follow-Up Care

**Return to the doctor if:**

- Infection signs appear or worsen
- The wound doesn't heal properly
- You develop fever
- Pain increases instead of decreasing
- You have concerns about your recovery

