## LESSON 1: UNDERSTANDING FAINTING

### What is Fainting?

Fainting is a temporary loss of consciousness caused by a decrease in blood flow to the brain. It usually lasts only a few seconds to a couple of minutes.

**Common Causes:**

- Standing for long periods
- Standing up too quickly
- Dehydration
- Low blood sugar
- Emotional stress or fear
- Overheating
- Pain

## LESSON 2: CHECKING FOR BREATHING

### First Priority: Check for Breathing

**What to do:**

- Immediately check if the person is breathing
- Look for chest rise and fall
- Listen for breath sounds
- Feel for air coming from the nose or mouth

### If the Person is NOT Breathing:

**Immediate Actions:**

- **Begin CPR immediately**
  - Start with chest compressions
  - Continue CPR until help arrives or the person starts breathing

- **Call the local emergency number (911 or your local emergency services)**
  - Call for help as soon as possible
  - If alone, call after starting CPR

**Important**: Not breathing after fainting is a life-threatening emergency.

### If the Person IS Breathing:

- Proceed with standard fainting first aid
- Continue to monitor breathing
- Provide appropriate care (see Lessons 3 and 4)

## LESSON 3: MANAGING FALL-RELATED INJURIES

### Assessing for Injuries

If the person was injured in a fall associated with fainting, check for:

- Bumps and bruises
- Cuts and scrapes
- Bleeding wounds
- Head injuries

### Treating Bumps, Bruises, or Cuts

**What to do:**

- Treat bumps, bruises, or cuts appropriately
- Clean any wounds
- Apply bandages as needed
- Apply cold compress to bumps and bruises

### Controlling Bleeding

**What to do:**

- Control the bleeding with direct pressure
- Use a clean cloth, gauze, or bandage
- Press firmly on the wound
- Continue pressure until bleeding stops
- Do not remove the cloth if it becomes soaked—add more layers on top

**Important**: Direct pressure is the most effective way to stop bleeding.

## LESSON 4: PROPER POSITIONING AFTER FAINTING

### Step 1: Position the Person on Their Back

**What to do:**

- If there aren't any injuries and the person is breathing, position the person on his or her back
- Ensure the airway is clear
- Make sure they are lying flat

**Why it's important:**

- Lying flat helps restore blood flow to the brain
- Promotes recovery

### Step 2: Raise the Person's Legs

**What to do:**

- Raise the person's legs above heart level
- Elevate about 12 inches if possible
- Use pillows, cushions, or folded clothing to prop up the legs

**Why it's important:**

- Helps blood flow back to the heart and brain
- Speeds up recovery
- Reduces the chance of fainting again

### Step 3: Loosen Constrictive Clothing

**What to do:**

- Loosen belts
- Loosen collars
- Loosen any other constrictive clothing

**Why it's important:**

- Improves breathing
- Improves circulation
- Helps the person feel more comfortable

## LESSON 5: PREVENTING REPEAT FAINTING

### Do Not Let the Person Get Up Too Quickly

**Important Rule**: To reduce the chance of fainting again, do not let the person get up too quickly.

### Safe Recovery Steps

**Step 1: Stay Lying Down**

- Keep the person lying flat with legs elevated
- Wait until they feel alert and normal

**Step 2: Sit Up Slowly**

- Help the person sit up slowly
- Wait a few minutes in sitting position

**Step 3: Stand Up Gradually**

- Only when ready, help them stand up slowly
- Support them as they stand
- Stay close in case they feel faint again

**If dizziness returns:**

- Have the person lie back down immediately
- Elevate the legs again

## LESSON 6: WHEN TO CALL EMERGENCY SERVICES

### Call Emergency Services If:

- The person is not breathing
- The person does not regain consciousness quickly
- The person has serious injuries from the fall
- The person has chest pain
- The person has difficulty breathing
- You are unsure about the person's condition

