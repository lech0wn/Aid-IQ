- Red streaks extending from the wound

**Action**: If you notice ANY of these signs, see a doctor immediately. Infections can become serious quickly if left untreated.

## LESSON 3: TREATING CLOSED WOUNDS

### Interventions for Closed Wounds

Closed wounds require different care since the skin is not broken. The focus is on reducing swelling, pain, and preventing further damage.

### R.I.C.E. Method Plus Immobilization

### 1. Ice Packs

- Apply ice wrapped in a cloth (never directly on skin)
- Use for 15-20 minutes at a time
- Repeat every 2-3 hours for the first 48 hours
- Reduces swelling and numbs pain

### 2. Compression

- Use an elastic bandage to wrap the injured area
- Wrap snugly but not too tight (you should be able to slip a finger under)
- Reduces swelling and provides support
- Remove if numbness, tingling, or increased pain occurs

### 3. Elevation

- Raise the injured area above heart level when possible
- Use pillows for support
- Helps reduce swelling by allowing fluid to drain

### 4. Immobilization

- Limit movement of the injured area
- Use splints or slings if appropriate
- Prevents further injury
- Allows healing to begin

### Watch for Signs of Infection or Complications

Even with closed wounds, watch for:

- Redness on the skin near the injury
- Increasing pain
- Warmth
- Swelling that doesn't improve or gets worse
- Drainage (which would indicate the wound has opened)

**Action**: If any of these signs appear, see a doctor immediately.

### When to Seek Medical Attention for Closed Wounds

See a doctor if:

- Pain is severe or doesn't improve
- You cannot move the injured area
- You suspect a fracture or broken bone
- The injured area looks deformed
- Swelling is severe
- Numbness or tingling develops

## LESSON 4: INFECTION PREVENTION AND RECOGNITION

### Why Infection Prevention Matters

Infections can:

- Delay healing
- Cause serious complications
- Lead to systemic illness
- Result in permanent damage or scarring
- In severe cases, become life-threatening

### Key Prevention Strategies

✓ Hand hygiene before and after wound care

✓ Proper cleaning of the wound

✓ Keeping the wound moist with ointment

✓ Covering the wound to protect from contamination

✓ Changing dressings regularly

✓ Monitoring for signs of infection

### Recognizing Infection: The 5 Cardinal Signs

Remember these five signs of infection:

1. **Redness** (Rubor)
2. **Heat/Warmth** (Calor)
3. **Swelling** (Tumor)
4. **Pain** (Dolor)
5. **Loss of function** (Functio laesa)

If you observe these signs increasing rather than decreasing over time, seek medical attention.

## LESSON 5: SPECIAL CONSIDERATIONS

### When to See a Doctor Immediately

Seek professional medical care if:

- The wound is deep or gaping
- Bleeding doesn't stop after 10 minutes of pressure
- The wound is from an animal or human bite
- The wound is contaminated with dirt, debris, or foreign objects you cannot remove
- The wound is on the face, hand, or joint
- You see signs of infection
- The wound was caused by a rusty or dirty object
- You haven't had a tetanus shot in 5+ years (for deep wounds)
- You have diabetes or a weakened immune system

### Wounds That Require Professional Care

- Deep cuts that may need stitches
- Puncture wounds (especially from nails, needles, or animal bites)
- Wounds with jagged edges that won't close on their own
- Wounds that won't stop bleeding
- Wounds showing signs of infection
- Wounds over joints that open when the joint moves

