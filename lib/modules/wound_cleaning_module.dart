## LESSON 1: UNDERSTANDING WOUNDS

### What is a Wound?

A wound is any damage or break in the surface of the skin or underlying tissues. Wounds can be classified into two main categories:

### Open Wounds

Open wounds are any internal or external injury that leaves internal tissues exposed to the external environment.

**Examples:**

- Cuts and lacerations
- Scrapes and abrasions
- Puncture wounds
- Surgical incisions

**Key Concern**: Open wounds are at higher risk for infection because internal tissues are exposed to bacteria and contaminants.

### Closed Wounds

Closed wounds are injuries where the skin remains intact, but underlying tissues are damaged.

**Common Causes:**

- Direct blunt trauma
- Falls
- Vehicle accidents
- Sports injuries

**Examples:**

- Bruises (contusions)
- Hematomas (blood collection under skin)
- Crush injuries

**Key Concern**: While the skin is not broken, internal damage can still be significant and may require medical attention.

## LESSON 2: TREATING OPEN WOUNDS

### Step-by-Step Intervention for Open Wounds

### Step 1: Wash Your Hands

**Why it's important**: Hand washing is the single most important step to avoid introducing infection into the wound.

**How to do it:**

- Use soap and clean water
- Wash for at least 20 seconds
- Dry with a clean towel
- If soap and water aren't available, use hand sanitizer with at least 60% alcohol

**Remember**: Always wash your hands BEFORE touching the wound or any wound care supplies.

### Step 2: Stop the Bleeding

**For Minor Cuts and Scrapes:**

- Most minor wounds stop bleeding on their own
- If bleeding continues, apply gentle pressure with a clean bandage or cloth
- Elevate the wound above heart level if possible
- Maintain pressure until bleeding stops

**Important**: Do not remove the cloth if blood soaks through—add more layers on top and continue pressure.

### Step 3: Clean the Wound

**Proper Cleaning Technique:**

- **Rinse with water**: Hold the wound under running tap water. This is the most effective way to reduce the risk of infection.

- **Wash around the wound**: Use soap to clean the skin AROUND the wound, but don't get soap directly IN the wound, as this can cause irritation.

- **Remove debris:**
  - Clean tweezers with rubbing alcohol
  - Gently remove any visible dirt or debris
  - If debris is embedded deeply or you cannot remove all of it, see a doctor for help

**What NOT to do:**

- Don't use hydrogen peroxide or iodine directly in the wound (can damage tissue)
- Don't scrub the wound vigorously
- Don't use cotton balls that can leave fibers in the wound

### Step 4: Apply Antibiotic Ointment or Petroleum Jelly

**Purpose**: Keeps the wound surface moist, which promotes healing and prevents scarring.

**How to apply:**

- Use only a thin layer
- Common options: antibiotic ointment (like Neosporin) or petroleum jelly (like Vaseline)
- Apply gently without rubbing

**Important Note**: If a rash appears after application, stop using the ointment. Some people are sensitive to certain ingredients that can cause mild allergic reactions.

### Step 5: Cover the Wound

**When to cover:**

- Cover wounds that are likely to get dirty
- Cover wounds that may rub against clothing
- Leave minor scratches uncovered if they're clean and unlikely to be contaminated

**How to cover:**

- Use an adhesive bandage for small wounds
- Use rolled gauze or gauze pads held with paper tape for larger wounds
- Ensure the covering is clean and sterile
- Don't wrap too tightly—allow for circulation

**Benefits of covering:**

- Keeps the wound clean
- Protects from bacteria
- Prevents further injury
- Keeps the wound moist for better healing

### Step 6: Change the Dressing

**Frequency**: At least once a day OR whenever the bandage becomes:

- Wet
- Dirty
- Loose or falling off

**How to change:**

- Wash your hands first
- Gently remove the old bandage
- Clean the wound again if needed
- Check for signs of healing or infection
- Apply fresh ointment
- Apply a new, clean bandage

### Step 7: Get a Tetanus Shot (If Needed)

**When a tetanus shot is recommended:**

- You haven't had a tetanus shot in the past 5 years
